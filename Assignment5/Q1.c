/*                          More on Iterative Control Statements
1. Write a program to print MySirG N times on the screen.
*/
#include<stdio.h>
int main()
{
    int num1,num2;
    printf("enter number=");
    scanf("%d",&num2);
    for(num1=1;num1<=num2;num1++)
    {
        printf("MySirG\n");
    }
    return 0;
}